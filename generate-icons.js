#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ICONS_DIR = './src/lib/components/Icons';
const INDEX_FILE = path.join(ICONS_DIR, 'index.ts');

function scanIconFiles() {
	console.log('🔍 Scanning Icons directory...');

	const files = fs.readdirSync(ICONS_DIR);
	const iconFiles = files.filter((file) => file.endsWith('.ts') && file !== 'index.ts');

	console.log(`📊 Found ${iconFiles.length} icon files`);

	const icons = [];

	for (const file of iconFiles) {
		const filePath = path.join(ICONS_DIR, file);
		const content = fs.readFileSync(filePath, 'utf8');

		// Extract icon name from filename (remove .ts extension)
		const iconName = path.basename(file, '.ts');

		// Extract all exported icon variants from the file
		const exportMatches = content.match(/export const (\w+) = icon\(/g);

		if (exportMatches) {
			const variants = exportMatches.map((match) => {
				const variant = match.match(/export const (\w+) = icon\(/)[1];
				return variant;
			});

			icons.push({
				name: iconName,
				file: file,
				variants: variants
			});
		}
	}

	return icons.sort((a, b) => a.name.localeCompare(b.name));
}

function generateIndexFile(icons) {
	console.log('📝 Generating index.ts file...');

	let content = `// Auto-generated icon barrel file
// Do not edit this file directly - run 'node generate-icons.js' to regenerate

`;

	// Add imports for each icon file
	for (const icon of icons) {
		content += `export {\n`;
		for (const variant of icon.variants) {
			content += `  ${variant},\n`;
		}
		content += `} from './${icon.name}.js';\n`;
	}

	fs.writeFileSync(INDEX_FILE, content);
	console.log(`✅ Generated ${INDEX_FILE}`);
}

