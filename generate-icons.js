function updatePackageJson() {
	console.log('📦 Updating package.json exports...');

	const packagePath = './package.json';
	const packageContent = fs.readFileSync(packagePath, 'utf8');
	const packageJson = JSON.parse(packageContent);

	// Initialize exports if it doesn't exist
	if (!packageJson.exports) {
		packageJson.exports = {};
	}

	// Add icons export
	packageJson.exports['./icons'] = {
		types: './dist/components/Icons/index.d.ts',
		default: './dist/components/Icons/index.js'
	};

	// Ensure the main export includes icons
	if (!packageJson.exports['./icons/*']) {
		packageJson.exports['./icons/*'] = {
			types: './dist/components/Icons/*.d.ts',
			default: './dist/components/Icons/*.js'
		};
	}

	fs.writeFileSync(packagePath, JSON.stringify(packageJson, null, 2));
	console.log('✅ Updated package.json exports');
}

function main() {
	console.log('🚀 Starting icon generation process...');

	try {
		const icons = scanIconFiles();
		generateIndexFile(icons);
		generateAgentDocumentation(icons);
		updatePackageJson();

		console.log('🎉 Icon generation completed successfully!');
		console.log(`📊 Generated exports for ${icons.length} icons`);
		console.log('💡 Run this script whenever you add new icons to update the exports');
	} catch (error) {
		console.error('❌ Error during icon generation:', error);
		process.exit(1);
	}
}

// Run the script
if (import.meta.url === `file://${process.argv[1]}`) {
	main();
}

export { scanIconFiles, generateIndexFile, generateAgentDocumentation, updatePackageJson };
